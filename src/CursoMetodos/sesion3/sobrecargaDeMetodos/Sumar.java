package CursoMetodos.sesion3.sobrecargaDeMetodos;

public class Sumar {

    public int sumarNumero(int numUno, int numDos){
        return numUno + numDos;
    }
    public double sumarNumero(double numUno, double numDos){
        return numUno + numDos;
    }

    public int sumarNumero(int numUno, int numDos, int numTres){
        return numUno + numDos;
    }

}
